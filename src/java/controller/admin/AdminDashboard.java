    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        //processRequest(request, response);
        AccountDAO a = new AccountDAO();
        int a_counter  = a.getTotalAccount();
        request.setAttribute("a_counter", a_counter);
        
        ProductDAO p = new ProductDAO();
        int p_counter = p.getTotalProduct();
        request.setAttribute("p_counter", p_counter);
        
        OrderDAO o = new OrderDAO();
        int o_counter = o.getTotalOrder();
        double revenue = o.getTotalRenevue();
        List<Order> listo = o.getListOrderByStatus(0);
        request.setAttribute("listo", listo);
        request.setAttribute("revenue", revenue);
        request.setAttribute("o_counter", o_counter);
        
        request.getRequestDispatcher("adminDashboard.jsp").forward(request, response);
    } 

